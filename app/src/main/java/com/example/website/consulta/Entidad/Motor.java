package com.example.website.consulta.Entidad;

public class Motor {
    private String marca, modelo, motor, cili1;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMotor() {
        return motor;
    }

    public void setMotor(String motor) {
        this.motor = motor;
    }

    public String getCili1() {
        return cili1;
    }

    public void setCili1(String cili1) {
        this.cili1 = cili1;
    }
}
